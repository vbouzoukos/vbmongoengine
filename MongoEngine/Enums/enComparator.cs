/// <summary>
/// Search comparison options
/// </summary>
public enum EnComparator
{
    /// <summary>
    /// The field is equal to search value
    /// </summary>
    EqualTo,
    /// <summary>
    /// The field is like the search value
    /// </summary>
    Like,
    /// <summary>
    /// The field is greater than search value
    /// </summary>
    GreaterThan,
    /// <summary>
    /// The field is Less than search value
    /// </summary>
    LessThan
}